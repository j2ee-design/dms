package com.xinho.bean;

public class Dormitory {
    private Integer id;

    private Integer dormId;

    private Integer apartId;

    private Integer dicDormType;

    private Integer chiefId;

    private Integer allBed;

    private Integer usedBed;

    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public Integer getDormId() {
        return dormId;
    }

    public void setDormId(Integer dormId) {
        this.dormId = dormId;
    }

    public Integer getApartId() {
        return apartId;
    }

    public void setApartId(Integer apartId) {
        this.apartId = apartId;
    }

    public Integer getDicDormType() {
        return dicDormType;
    }

    public void setDicDormType(Integer dicDormType) {
        this.dicDormType = dicDormType;
    }

    public Integer getChiefId() {
        return chiefId;
    }

    public void setChiefId(Integer chiefId) {
        this.chiefId = chiefId;
    }

    public Integer getAllBed() {
        return allBed;
    }

    public void setAllBed(Integer allBed) {
        this.allBed = allBed;
    }

    public Integer getUsedBed() {
        return usedBed;
    }

    public void setUsedBed(Integer usedBed) {
        this.usedBed = usedBed;
    }
}